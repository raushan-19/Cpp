#include <iostream>
using namespace std;
int main()
{
    int i = 1;               // Declare a variable 'i' and initialize it with 1

    while (i <= 10)          // Loop continues as long as i is less than or equal to 10
    {
        cout << i << " ";    // Print the current value of i followed by a space
        i++;                 // Increase the value of i by 1 (i = i + 1)
    }
}