#include <iostream>          // Includes the standard input-output library (for cout, cin, etc.)
using namespace std;         // Allows us to use names like cout without writing std::cout

int main()                   // Program execution starts from main() function
{
    cout << "hello world"    // Prints "hello world" on the screen
         << "\trun baby run"; // \t adds a horizontal tab, then prints "run baby run"

    cout << endl;            // Prints a newline (moves cursor to next line)

    cout << "\n";            // Another way to print a newline using escape sequence

    cout << "hello baby";    // Prints "hello baby" on the screen

}                            // End of main() function and program finishes here
